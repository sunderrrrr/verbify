-- 1. Таблица заданий (все в одной таблице)
CREATE TABLE practice_tasks (
    id SERIAL PRIMARY KEY,
    ege_number INT NOT NULL, -- 9, 10, 11... прямо номер задания
    text TEXT NOT NULL,
    hint TEXT,
    correct_answer VARCHAR(255) NOT NULL,
    difficulty INT DEFAULT 1,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 2. Таблица попыток
CREATE TABLE practice_attempts (
    id SERIAL PRIMARY KEY,
    user_id INT NOT NULL REFERENCES users(id),
    ege_number INT NOT NULL, -- номер задания (9, 10, 11...)
    tasks_count INT NOT NULL,
    correct_count INT DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 3. Таблица ответов
CREATE TABLE practice_answers (
    id SERIAL PRIMARY KEY,
    attempt_id INT NOT NULL REFERENCES practice_attempts(id) ON DELETE CASCADE,
    task_id INT NOT NULL REFERENCES practice_tasks(id),
    user_answer VARCHAR(255) NOT NULL,
    is_correct BOOLEAN NOT NULL,
    comment TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Индексы
CREATE INDEX idx_practice_attempts_user ON practice_attempts(user_id);
CREATE INDEX idx_practice_attempts_ege ON practice_attempts(ege_number);
CREATE INDEX idx_practice_tasks_ege ON practice_tasks(ege_number);

